package com.ssafy.vo;

public class Allergy {
	String acode, aname;

	public Allergy(String acode, String aname) {
		this.acode = acode;
		this.aname = aname;
	}

	public Allergy() {
	}

	public String getAcode() {
		return acode;
	}

	public void setAcode(String acode) {
		this.acode = acode;
	}

	public String getAname() {
		return aname;
	}

	public void setAname(String aname) {
		this.aname = aname;
	}
	
	
	
	
}	
